# Exibe números de 1 a 10 usando for
# Esse comentário explica o objetivo geral do código

# Loop for que percorre números de 1 até 10
# range(1, 11) começa no 1 e vai até 10
# O 11 não é incluído (o limite final do range é sempre exclusivo)
for numero in range(1, 11):

    # Imprime o valor atual da variável 'numero' a cada repetição do loop
    print(numero)
